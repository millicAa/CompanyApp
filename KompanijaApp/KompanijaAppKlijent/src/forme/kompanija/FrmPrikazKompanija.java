    private void srediFormu() {
        try {
            //List<Kompanija> lk = Kontroler.getInstance().vratiKompanije();
            TransferObjekatZahtev toZahtev = new TransferObjekatZahtev();
            toZahtev.setOperacija(Operacija.VRATI_KOMPANIJE);
            Komunikacija.getInstance().posalji(toZahtev);
            TransferObjekatOdgovor toOdgovor = Komunikacija.getInstance().procitaj();
            List<Kompanija> lk =  (List<Kompanija>) toOdgovor.getRezultat();
            DefaultTableModel model = (DefaultTableModel) jtblKompanije.getModel();
            for (Kompanija k : lk) {
                Object[] red = new Object[6];
                red[0] = k.getKompanijaID();
                red[1] = k.getNaziv();
                red[2] = k.getMaticniBroj();
                red[3] = k.getUlica();
                red[4] = k.getBroj();
                red[5] = k.getMesto();
                // Dodavanje novog reda u tabelu
                model.addRow(red);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
