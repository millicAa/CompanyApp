    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed
        try {
            String naziv = jtxtNaziv.getText().trim();
            String maticniBroj = jtxtMaticniBroj.getText().trim();
            String ulica = jtxtUlica.getText().trim();
            String broj = jtxtBroj.getText().trim();
            Mesto mesto = (Mesto) jcbMesto.getSelectedItem();
            // TODO Valudacija
            Kompanija k = new Kompanija(naziv, maticniBroj, ulica, broj, mesto);
            //Kontroler.getInstance().sacuvajKompaniju(k);
            TransferObjekatZahtev toZahtev = new TransferObjekatZahtev();
            toZahtev.setOperacija(Operacija.SACUVAJ_KOMPANIJU);
            toZahtev.setParametar(k);
            Komunikacija.getInstance().posalji(toZahtev);
            TransferObjekatOdgovor toOdgovor = Komunikacija.getInstance().procitaj();
            if (toOdgovor.getIzuzetak() == null) {
                JOptionPane.showMessageDialog(this, "Kompanija je sacuvana");
            } else {
                throw new Exception(toOdgovor.getIzuzetak());
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jbtnSacuvajActionPerformed


