    private void srediFormu() {
        try {
            //List<Mesto> lm = Kontroler.getInstance().vratiMesta();
            TransferObjekatZahtev toZahtev = new TransferObjekatZahtev();
            toZahtev.setOperacija(Operacija.VRATI_MESTA);
            Komunikacija.getInstance().posalji(toZahtev);
            TransferObjekatOdgovor toOdgovor = Komunikacija.getInstance().procitaj();
            List<Mesto> lm = (List<Mesto>) toOdgovor.getRezultat();
            jcbMesto.removeAllItems();
            for (Mesto m : lm) {
                jcbMesto.addItem(m);
            }
            
            Kompanija k = (Kompanija) Sesija.getInstance().get("izabrana_kompanija");
            if (k != null) {
                // Operacija izmene ili brisanja
                jbtnSacuvaj.setVisible(false);
                jtxtNaziv.setText(k.getNaziv());
                jtxtMaticniBroj.setText(k.getMaticniBroj());
                jtxtUlica.setText(k.getUlica());
                jtxtBroj.setText(k.getBroj());
                jcbMesto.setSelectedItem(k.getMesto());
                Sesija.getInstance().remove("izabrana_kompanija");
            } else {
                // Operacija unosa
                jbtnIzmeni.setVisible(false);
                jbtnObrisi.setVisible(false);
            }
            
            
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
